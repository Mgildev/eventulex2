<h4>¿Con cuánta antelación puedo comprar una entrada?</h4>

<p>Las entradas se pueden comprar hasta 1 día antes del evento.</p>

<h4>¿Cómo puedo entrar en el evento con la entrada comprada?</h4>

<p>En los controles de acceso al recinto mostrando la entrada impresa o desde el teléfono móvil.</p>

<h4>¿Puedo realizar reservas de entradas?</h4>

<p>No se pueden hacer reservas de entradas.</p>

<h4>¿Se aplican descuentos en la compra de entradas por Internet?</h4>

<p>Los descuentos dependen de la organización de los diversos eventos, no depende de Eventulex.</p>

<h4>¿Cómo puedo anular una entrada comprado por Internet?</h4>

<p>Eventulex no permite anular las entradas, solo se devolveria el dinero en caso de anulación del evento, asegurate antes de comprar las entradas.</p>
